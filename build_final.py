def lin_interp(anchors):
    ak=sorted(anchors); res={}
    for y in YEARS:
        if y<=ak[0]:    res[y]=anchors[ak[0]]
        elif y>=ak[-1]: res[y]=anchors[ak[-1]]
        else:
            for i in range(len(ak)-1):
                if ak[i]<=y<=ak[i+1]:
                    t=(y-ak[i])/(ak[i+1]-ak[i])
                    res[y]=anchors[ak[i]]+t*(anchors[ak[i+1]]-anchors[ak[i]]); break
    return res


        d=step8(sc,y)
        tgt=dashboard[sc][y]
        ratio,amount=ccus_balance(d,tgt)
        net=d['curve_tech']-amount
        actual_ci=net/d['P'] if d['P']>0 else 0
        ALL[sc][y]={**d,'target_ci':tgt,'ccus_ratio':ratio,
                    'ccus_amount':amount,'net_emission':net,'actual_ci':actual_ci}


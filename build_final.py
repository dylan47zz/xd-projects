# ─── 核心：Step8完整计算（负值保护）─────────────────────────────────
def step8(sc, year):
    P=prod[sc][year]
    eaf=eaf_pct[sc][year]/100; h2=h2_pct[sc][year]/100; bf=1-eaf-h2
    P_bf,P_eaf,P_h2 = P*bf,P*eaf,P*h2
    scrap_bf=P_bf*0.30; scrap_eaf=P_eaf*1.10; scrap_h2=P_h2*0.50
    scrap_tot=scrap_bf+scrap_eaf+scrap_h2
    h2_ore=(P_h2-scrap_h2/1.1)*1.053*1.65
    total_elec=P_bf*BF_ELEC+P_eaf*EAF_ELEC+P_h2*H2_ELEC
    green_elec=total_elec*green_pct[sc][year]

    cp=P*BASE_CI
    # 原料：允许负值（表示废钢减少时降碳为负，即排放增加）
    rr=(scrap_tot-BASE_SCRAP_TOT)/1.1*DIFF_CI
    cr=cp-rr
    # 短流程：允许负值（同理）
    er=(scrap_eaf-BASE_EAF_SCRAP)*H2_ORE_FACTOR
    ce=cr-er
    hr=max(0,(h2_ore-BASE_H2_ORE)/1.65*H2_ORE_FACTOR)
    ch=ce-hr
    gr=green_elec*GRID_FACTOR/1000
    cg=ch-gr
    cum=tech_cum[sc][year]+0.066
    tr=P*cum; ct=cg-tr

    return {'P':P,'scrap_tot':scrap_tot,'scrap_eaf':scrap_eaf,'h2_ore':h2_ore,
            'P_bf':P_bf,'P_eaf':P_eaf,'P_h2':P_h2,
            'total_elec':total_elec,'green_elec':green_elec,
            'curve_prod':cp,'raw_reduce':rr,'curve_raw':cr,
            'eaf_reduce':er,'curve_eaf':ce,
            'h2_reduce':hr,'curve_h2':ch,
            'green_reduce':gr,'curve_green':cg,
            'tech_reduce':tr,'cum_tech':cum,'curve_tech':ct}

# ─── CCUS配平 ───────────────────────────────────────────────────────────
# 核心公式：CCUS_ratio = 1 - (target_CI × P) / curve_tech
# 约束：0 ≤ ratio ≤ 0.98
def ccus_balance(d, target_ci):
    P,ct=d['P'],d['curve_tech']
    if ct<=0: return 0.0,0.0
    target_net=target_ci*P
    ratio=1.0-target_net/ct
    ratio=max(0.0,min(0.98,ratio))
    return ratio,ct*ratio


# ─── 核心计算函数（V11公式逻辑）──────────────────────────────────────
def calc_curve_tech(sc, year, eaf_p, h2_p, green_p, cum_new_tech):
    P = prod[sc][year]
    eaf=eaf_p/100; h2=h2_p/100; bf=1-eaf-h2
    P_bf,P_eaf,P_h2 = P*bf,P*eaf,P*h2
    scrap_bf=P_bf*0.30; scrap_eaf=P_eaf*1.10; scrap_h2=P_h2*0.50
    scrap_tot=scrap_bf+scrap_eaf+scrap_h2
    h2_ore=(P_h2-scrap_h2/1.1)*1.053*1.65
    total_elec=P_bf*BF_E+P_eaf*EAF_E+P_h2*H2_E
    green_elec=total_elec*green_p

    cp=P*BASE_CI
    rr=(scrap_tot-BASE_SCRAP_TOT)/1.1*DIFF_CI
    cr=cp-rr
    er=(scrap_eaf-BASE_EAF_SCRAP)*H2_ORE_FACTOR
    ce=cr-er
    hr=max(0,(h2_ore-BASE_H2_ORE)/1.65*H2_ORE_FACTOR)
    ch=ce-hr
    gr=green_elec*GRID/1000
    cg=ch-gr
    tr=P*cum_new_tech
    ct=cg-tr
    return ct, P


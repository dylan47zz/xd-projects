# Step 8 模拟计算（逻辑与Excel公式完全对应）
# ────────────────────────────────────────────────────────────
def step8_calc(scenario, year):
    P   = prod[scenario][year]
    eaf = eaf_pct[scenario][year] / 100
    h2  = h2_pct[scenario][year]  / 100
    bf  = 1 - eaf - h2

    P_bf, P_eaf, P_h2 = P*bf, P*eaf, P*h2

    scrap_bf  = P_bf  * 0.30
    scrap_eaf = P_eaf * 1.10
    scrap_h2  = P_h2  * 0.50
    scrap_tot = scrap_bf + scrap_eaf + scrap_h2
    h2_ore    = (P_h2 - scrap_h2/1.1) * 1.053 * 1.65

    total_elec = P_bf*BF_ELEC + P_eaf*EAF_ELEC + P_h2*H2_ELEC
    green_elec = total_elec * green_pct[scenario][year]

    # 8.1 产量变化
    curve_prod = P * BASE_CI

    # 8.2 原料优化 — 废钢总量vs基准
    raw_reduce = (scrap_tot - BASE_SCRAP_TOT) / 1.1 * DIFF_CI
    curve_raw  = curve_prod - raw_reduce

    # 8.3 短流程提升 — 短流程废钢vs基准
    eaf_reduce = (scrap_eaf - BASE_EAF_SCRAP) * H2_ORE_FACTOR
    curve_eaf  = curve_raw - eaf_reduce

    # 8.4 氢冶金提升
    h2_reduce  = (h2_ore - BASE_H2_ORE) / 1.65 * H2_ORE_FACTOR
    curve_h2   = curve_eaf - h2_reduce

    # 8.5 绿电
    green_reduce = green_elec * GRID_FACTOR / 1000
    curve_green  = curve_h2 - green_reduce

    # 8.6 技术（累计+0.066基准）
    cum = tech_cum[scenario][year] + 0.066
    tech_reduce  = P * cum
    curve_tech   = curve_green - tech_reduce

    return {
        'P': P, 'P_bf': P_bf, 'P_eaf': P_eaf, 'P_h2': P_h2,
        'scrap_tot': scrap_tot, 'scrap_eaf': scrap_eaf, 'h2_ore': h2_ore,
        'total_elec': total_elec, 'green_elec': green_elec,
        'curve_prod': curve_prod,
        'raw_reduce': raw_reduce,   'curve_raw': curve_raw,
        'eaf_reduce': eaf_reduce,   'curve_eaf': curve_eaf,
        'h2_reduce':  h2_reduce,    'curve_h2':  curve_h2,
        'green_reduce': green_reduce, 'curve_green': curve_green,
        'tech_reduce': tech_reduce,   'curve_tech': curve_tech,
        'cum_tech': cum,
    }

def calc_ccus(d, target_ci):
    """配平CCUS比例，使净排放/产量=目标CI"""
    P, ct = d['P'], d['curve_tech']
    if ct <= 0:
        return 0.0, 0.0
    target_net = target_ci * P
    ratio = 1.0 - target_net / ct
    ratio = max(0.0, min(0.98, ratio))
    amount = ct * ratio
    return ratio, amount


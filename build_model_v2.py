def build_tech_new(scenario):
    """
    积极: 按时序，full scale
    适度: 延迟1年，scale 0.85
    保守: 延迟3年，scale 0.65
    """
    delays = {'积极情景':0, '适度情景':1, '保守情景':3}
    scales = {'积极情景':1.0,'适度情景':0.85,'保守情景':0.65}
    d, sc = delays[scenario], scales[scenario]
    out = {y:0.0 for y in YEARS}
    for base_y, val in TECH_BASE.items():
        act_y = base_y + d
        if 2025 <= act_y <= 2060:
            out[act_y] = out.get(act_y, 0.0) + val * sc
    return out


def simulate_step8(scenario, year):
    """计算某情景某年份的低碳工艺碳排放曲线（不含CCUS）"""
    P = prod[scenario][year]               # 产量万吨
    eaf = eaf_pct[scenario][year] / 100   # 短流程占比
    h2  = h2_pct[scenario][year]  / 100   # 氢冶金占比
    bf  = 1 - eaf - h2                    # 长流程占比

    P_bf  = P * bf
    P_eaf = P * eaf
    P_h2  = P * h2

    # Step 5: 废钢消耗
    scrap_bf  = P_bf  * 0.30
    scrap_eaf = P_eaf * 1.10
    scrap_h2  = P_h2  * 0.50
    scrap_tot = scrap_bf + scrap_eaf + scrap_h2

    # 氢冶金矿石消耗
    h2_ore = (P_h2 - scrap_h2 / 1.1) * 1.053 * 1.65

    # Step 6: 总耗电量 & 绿电量
    total_elec = P_bf * BF_ELEC + P_eaf * EAF_ELEC + P_h2 * H2_ELEC  # 万kWh
    green_elec = total_elec * green_pct[scenario][year]

    # Step 8 瀑布流
    # 8.1 产量变化
    curve_prod = P * BASE_CI

    # 8.2 原料结构优化
    delta_scrap = scrap_tot - BASE_SCRAP_TOT
    raw_reduce = (delta_scrap / 1.1) * DIFF_CI
    curve_raw  = curve_prod - raw_reduce

    # 8.3 短流程提升
    delta_eaf_scrap = scrap_eaf - BASE_EAF_SCRAP
    eaf_reduce = delta_eaf_scrap * H2_ORE_FACTOR
    curve_eaf  = curve_raw - eaf_reduce

    # 8.4 氢冶金提升
    delta_h2_ore = h2_ore - BASE_H2_ORE
    h2_reduce = (delta_h2_ore / 1.65) * H2_ORE_FACTOR
    curve_h2  = curve_eaf - h2_reduce

    # 8.5 绿电
    green_reduce = green_elec * GRID_FACTOR / 1000
    curve_green  = curve_h2 - green_reduce

    # 8.6 低碳工艺技术
    # 累计技术降碳潜力 + 基准0.066
    cum_tech = tech_cum[scenario][year] + 0.066
    tech_reduce = P * cum_tech
    curve_tech  = curve_green - tech_reduce    # 低碳工艺碳排放曲线

    return curve_tech, {
        'P': P, 'eaf_pct': eaf*100, 'h2_pct': h2*100, 'bf_pct': bf*100,
        'scrap_tot': scrap_tot, 'scrap_eaf': scrap_eaf, 'h2_ore': h2_ore,
        'green_elec': green_elec, 'total_elec': total_elec,
        'curve_prod': curve_prod, 'raw_reduce': raw_reduce,
        'eaf_reduce': eaf_reduce, 'h2_reduce': h2_reduce,
        'green_reduce': green_reduce, 'tech_reduce': tech_reduce,
        'curve_tech': curve_tech,
    }

def calc_ccus_ratio(curve_tech, target_ci, P):
    """
    CCUS配平：
    net_emission = curve_tech * (1 - ccus_ratio) = target_ci * P
    ccus_ratio = 1 - target_ci * P / curve_tech
    约束：0 ≤ ccus_ratio ≤ 0.99
    """
    if curve_tech <= 0:
        return 0.0
    net_target = target_ci * P
    ratio = 1 - net_target / curve_tech
    return max(0.0, min(0.99, ratio))


def build_tech_schedule(scenario):
    """根据情景生成逐年新增技术降碳潜力"""
    # 积极：按时序全部部署，额外加速
    # 适度：略有延迟，数量适中
    # 保守：延迟2-4年，数量减少
    delays = {'积极情景': 0, '适度情景': 1, '保守情景': 3}
    scale  = {'积极情景': 1.0, '适度情景': 0.85, '保守情景': 0.65}
    
    delay = delays[scenario]
    sc    = scale[scenario]
    
    schedule = {y: 0.0 for y in YEARS}
    for base_yr, val in TECH_SCHEDULE_BASE.items():
        actual_yr = base_yr + delay
        if actual_yr in schedule:
            schedule[actual_yr] = val * sc
    
    return schedule

tech_new = {s: build_tech_schedule(s) for s in ['积极情景','适度情景','保守情景']}


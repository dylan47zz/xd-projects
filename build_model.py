def interp_smooth(anchors):
    """给定锚点字典 {年份:值}，用线性插值填满2024-2060"""
    years = sorted(anchors.keys())
    vals  = [anchors[y] for y in years]
    result = {}
    for y in YEARS:
        if y <= years[0]:
            result[y] = vals[0]
        elif y >= years[-1]:
            result[y] = vals[-1]
        else:
            # 找到插值区间
            for i in range(len(years)-1):
                if years[i] <= y <= years[i+1]:
                    t = (y - years[i]) / (years[i+1] - years[i])
                    result[y] = vals[i] + t * (vals[i+1] - vals[i])
                    break
    return result


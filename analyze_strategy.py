    """计算 curve_tech / (target_CI × P) 的比值"""
    P = prod[sc][year]
    d = step8_calc(P, eaf_p, h2_p, green_p, cum_tech)
    target = dashboard[sc][year] * P
    return d['curve_tech'] / target if target > 0 else 1


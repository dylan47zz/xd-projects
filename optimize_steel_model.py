def update_scenario_sheet(ws, scenario_name, production_key, scrap_key):
    """更新单个情景Sheet"""
    
    print(f"\n  处理 {scenario_name}...")
    
    # 找到年份行 (通常是Row 4)
    year_row = 4
    year_col_start = 3  # C列开始
    
    # 确认年份行
    if ws.cell(year_row, year_col_start).value != 2024:
        print(f"    警告: 年份行位置可能不对")
        return
    
    # 1. 更新产量 (Step 3, 假设在Row 12)
    prod_row = None
    for row in range(10, 15):
        cell_val = ws.cell(row, 2).value
        if cell_val and "粗钢产量" in str(cell_val):
            prod_row = row
            break
    
    if prod_row:
        print(f"    更新产量数据 (Row {prod_row})...")
        prod_dict = production_data[production_key]
        
        for year in range(2024, 2061):
            col = year_col_start + (year - 2024)
            if year in prod_dict:
                # 转换为万吨
                new_val = prod_dict[year] * 10000
                ws.cell(prod_row, col, new_val)
    
    # 2. 更新废钢资源量参考数据 (需要找到具体位置)
    # 根据文档,废钢数据在Step 5区域
    scrap_row = None
    for row in range(21, 30):
        cell_val = ws.cell(row, 2).value
        if cell_val and "废钢" in str(cell_val) and "总量" in str(cell_val):
            scrap_row = row
            break
    
    if scrap_row:
        print(f"    更新废钢资源量 (Row {scrap_row})...")
        scrap_dict = scrap_data[scrap_key]
        
        for year in range(2024, 2061):
            col = year_col_start + (year - 2024)
            if year in scrap_dict:
                ws.cell(scrap_row, col, scrap_dict[year])
    
    print(f"    {scenario_name} 更新完成")

# 更新三个情景Sheet

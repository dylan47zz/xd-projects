# ─── 精确复现V11公式计算curve_tech ──────────────────────────────────
def calc_curve_tech(sc, year):
    P = prod[sc][year]
    eaf=eaf_pct[sc][year]/100; h2=h2_pct[sc][year]/100; bf=1-eaf-h2
    P_bf,P_eaf,P_h2 = P*bf,P*eaf,P*h2
    scrap_bf=P_bf*0.30; scrap_eaf=P_eaf*1.10; scrap_h2=P_h2*0.50
    scrap_tot=scrap_bf+scrap_eaf+scrap_h2
    h2_ore=(P_h2-scrap_h2/1.1)*1.053*1.65
    total_elec=P_bf*BF_E+P_eaf*EAF_E+P_h2*H2_E
    green_elec=total_elec*green_pct[sc][year]

    cp=P*BASE_CI
    rr=(scrap_tot-BASE_SCRAP_TOT)/1.1*DIFF_CI   # 可负
    cr=cp-rr
    er=(scrap_eaf-BASE_EAF_SCRAP)*H2_ORE_FACTOR  # 可负
    ce=cr-er
    hr=max(0,(h2_ore-BASE_H2_ORE)/1.65*H2_ORE_FACTOR)
    ch=ce-hr
    gr=green_elec*GRID/1000
    cg=ch-gr
    # V11中Row56 = P × Row37（累计新增，不含基准0.066）
    cum_new = sum(tech_new[sc].get(y,0) for y in range(2025, year+1))
    tr=P*cum_new
    ct=cg-tr

    return {'P':P,'scrap_tot':scrap_tot,'scrap_eaf':scrap_eaf,'h2_ore':h2_ore,
            'total_elec':total_elec,'green_elec':green_elec,
            'curve_prod':cp,'raw_reduce':rr,'curve_raw':cr,
            'eaf_reduce':er,'curve_eaf':ce,'h2_reduce':hr,'curve_h2':ch,
            'green_reduce':gr,'curve_green':cg,
            'cum_new':cum_new,'tech_reduce':tr,'curve_tech':ct}

# ─── CCUS动态比例计算 ──────────────────────────────────────────────────
def calc_ccus(ct, tgt_ci, P):
    if ct <= 0 or tgt_ci*P >= ct:
        return 0.0  # 无法配平
    ratio = 1.0 - tgt_ci*P/ct
    return min(max(0.0, ratio), 0.98)


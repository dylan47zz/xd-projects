# 先模拟计算curve_tech（用V11公式逻辑复现）
def simulate_curve_tech(sc, year, eaf_p, h2_p, green_p, new_tech_annual):
    """
    精确复现V11的Excel公式逻辑
    Row 37 = 累计新增（不含基准0.066）
    Row 38 = Row37 + 0.066  
    Row 56 = 产量 × Row37（低碳工艺降碳潜力）
    Row 57 = Row54 - Row56  (curve_tech)
    
    Row 36 = 逐年新增技术（手动输入）
    Row 37 = 上年累计 + 当年新增
    Row 38 = Row37 + 0.066
    注意：Row56用的是Row37（不含基准0.066），不是Row38！
    """
    P = prod[sc][year]
    eaf = eaf_p/100; h2 = h2_p/100; bf = 1-eaf-h2
    P_bf,P_eaf,P_h2 = P*bf,P*eaf,P*h2
    
    scrap_bf  = P_bf*0.30
    scrap_eaf = P_eaf*1.10
    scrap_h2  = P_h2*0.50
    scrap_tot = scrap_bf+scrap_eaf+scrap_h2
    h2_ore    = (P_h2-scrap_h2/1.1)*1.053*1.65
    
    total_elec = P_bf*450+P_eaf*550+P_h2*500
    green_elec = total_elec*green_p
    
    # Row 37: 累计新增技术（不含基准）
    # Excel中Row37是逐年累积，2024年=0
    cum_new = sum(new_tech_annual.get(y,0) for y in range(2024, year+1))
    if year == 2024: cum_new = 0  # 2024年新增=0
    
    # Row 41 产量变化碳排放曲线
    curve_prod = P * 1.95
    
    # Row 44 原料优化降碳潜力（可以为负）
    BASE_SCRAP_TOT=38394.44
    raw_reduce = (scrap_tot-BASE_SCRAP_TOT)/1.1*1.35
    curve_raw = curve_prod - raw_reduce
    
    # Row 47 短流程提升降碳潜力（可以为负）
    BASE_EAF_SCRAP=11055.99
    eaf_reduce = (scrap_eaf-BASE_EAF_SCRAP)*1.26
    curve_eaf = curve_raw - eaf_reduce
    
    # Row 50 氢冶金提升降碳潜力
    BASE_H2_ORE=952.52
    h2_reduce = max(0,(h2_ore-BASE_H2_ORE)/1.65*1.26)
    curve_h2 = curve_eaf - h2_reduce
    
    # Row 53 绿电降碳
    green_reduce = green_elec*0.5306/1000
    curve_green = curve_h2 - green_reduce
    
    # Row 56 低碳工艺降碳（用Row37，不含基准0.066）
    tech_reduce_new = P * cum_new
    # 注意：Row56 = P × Row37，而Row38=Row37+0.066
    # 所以curve_tech = curve_green - P×cum_new
    # 但V11里已经有0.066的基准降碳，通过Row38反映在某处...
    # 实际上V11用Row37（累计新增）算低碳工艺降碳潜力
    # Row38只是"显示用"的累计值（含基准）
    # 真正的降碳计算用Row37
    
    curve_tech = curve_green - tech_reduce_new
    
    return {
        'P':P, 'scrap_tot':scrap_tot, 'scrap_eaf':scrap_eaf, 'h2_ore':h2_ore,
        'curve_prod':curve_prod,'raw_reduce':raw_reduce,'curve_raw':curve_raw,
        'eaf_reduce':eaf_reduce,'curve_eaf':curve_eaf,
        'h2_reduce':h2_reduce,'curve_h2':curve_h2,
        'green_reduce':green_reduce,'curve_green':curve_green,
        'tech_reduce_new':tech_reduce_new,'curve_tech':curve_tech,
        'cum_new':cum_new,'green_pct':green_p
    }


# ─── 计算CCUS配平比例 ────────────────────────────────────────────────
def calc_all_ccus():
    ccus_ratios = {}
    for sc in ['积极情景','适度情景','保守情景']:
        ccus_ratios[sc] = {}
        for y in YEARS:
            P = prod[sc][y]
            eaf=eaf_pct[sc][y]/100; h2=h2_pct[sc][y]/100; bf=1-eaf-h2
            P_bf,P_eaf,P_h2=P*bf,P*eaf,P*h2
            scrap_bf=P_bf*0.30; scrap_eaf=P_eaf*1.10; scrap_h2=P_h2*0.50
            scrap_tot=scrap_bf+scrap_eaf+scrap_h2
            h2_ore=(P_h2-scrap_h2/1.1)*1.053*1.65
            total_elec=P_bf*BF_E+P_eaf*EAF_E+P_h2*H2_E
            green_elec=total_elec*green_pct[sc][y]
            cp=P*BASE_CI
            rr=(scrap_tot-BASE_SCRAP_TOT)/1.1*DIFF_CI
            er=(scrap_eaf-BASE_EAF_SCRAP)*H2_ORE_FACTOR
            hr=max(0,(h2_ore-BASE_H2_ORE)/1.65*H2_ORE_FACTOR)
            gr=green_elec*GRID/1000
            tr=P*tech_cum[sc][y]
            ct=cp-rr-er-hr-gr-tr
            tgt=dashboard[sc][y]
            if ct>tgt*P and P>0:
                ratio=1-tgt*P/ct
            else:
                ratio=0.0
            ccus_ratios[sc][y]=min(max(0,ratio),0.98)
    return ccus_ratios

ccus_ratios = calc_all_ccus()

